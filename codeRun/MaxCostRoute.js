/*
  3. Вывести маршрут максимальной стоимости
  
  В левом верхнем углу прямоугольной таблицы размером N×M находится черепашка. 
  В каждой клетке таблицы записано некоторое число. Черепашка может перемещаться 
  вправо или вниз, при этом маршрут черепашки заканчивается в правом нижнем углу таблицы.
  
  Подсчитаем сумму чисел, записанных в клетках, через которую проползла черепашка (включая 
  начальную и конечную клетку). Найдите наибольшее возможное значение этой суммы и маршрут, 
  на котором достигается эта сумма.
  
  Формат ввода
    В первой строке входных данных записаны два натуральных числа N и M, 
    не превосходящих 100 — размеры таблицы. Далее идет N строк, каждая из которых 
    содержит M чисел, разделенных пробелами — описание таблицы. Все числа в клетках 
    таблицы целые и могут принимать значения от 0 до 100.

  Формат вывода
    Первая строка выходных данных содержит максимальную возможную сумму, вторая — маршрут,
    на котором достигается эта сумма. Маршрут выводится в виде последовательности, которая
    должна содержать N-1 букву D, означающую передвижение вниз и M-1 букву R, означающую
    передвижение направо. Если таких последовательностей несколько, необходимо вывести 
    ровно одну (любую) из них.
*/
function findMaxPathSum(input) {
  const [N, M] = input[0].split(" ").map(Number);

  const table = [];
  for (let i = 1; i <= N; i++) {
    table.push(input[i].split(" ").map(Number));
  }

  const dp = [];
  for (let i = 0; i < N; i++) {
    dp.push(new Array(M).fill(0));
  }

  dp[0][0] = table[0][0];

  // Первая строка
  for (let j = 1; j < M; j++) {
    dp[0][j] = dp[0][j - 1] + table[0][j];
  }

  // Первый столбец
  for (let i = 1; i < N; i++) {
    dp[i][0] = dp[i - 1][0] + table[i][0];
  }

  // Остальные клетки
  for (let i = 1; i < N; i++) {
    for (let j = 1; j < M; j++) {
      dp[i][j] = Math.max(dp[i - 1][j], dp[i][j - 1]) + table[i][j];
    }
  }

  const path = [];
  let i = N - 1;
  let j = M - 1;

  while (i > 0 || j > 0) {
    if (i === 0) {
      path.push("R");
      j--;
    } else if (j === 0) {
      path.push("D");
      i--;
    } else {
      if (dp[i][j - 1] > dp[i - 1][j]) {
        path.push("R");
        j--;
      } else {
        path.push("D");
        i--;
      }
    }
  }

  return {
    maxSum: dp[N - 1][M - 1],
    path: path.reverse().join(" "),
  };
}

