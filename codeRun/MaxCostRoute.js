if (require.main === module) {
  const tests = [
    {
      input: ["2 2", "1 2", "3 4"],
      expected: { maxSum: 8, path: "D R" },
    },
    {
      input: [
        "5 5",
        "9 9 9 9 9",
        "3 0 0 0 0",
        "9 9 9 9 9",
        "6 6 6 6 8",
        "9 9 9 9 9",
      ],
      expected: { maxSum: 74, path: "D D R R R R D D" },
    },
  ];

  tests.forEach((test, index) => {
    const result = findMaxPathSum(test.input);
    console.log(`Тест ${index + 1}:`);
    console.log(
      `  Сумма: ${result.maxSum} (ожидалось ${test.expected.maxSum})`,
    );
    console.log(`  Путь: ${result.path} (ожидалось ${test.expected.path})`);
    console.log(
      `  Результат: ${result.maxSum === test.expected.maxSum && result.path === test.expected.path ? "✓ OK" : "✗ FAIL"}`,
    );
    console.log();
  });
}
